//sort by fix price ascending
function sortByFixPrice(){
    let tempArr = productArr;
    for(let i = 0; i < tempArr.length-1; i++){
        for(let j = i+1; j < tempArr.length; j++){
            if(tempArr[i].fixPrice > tempArr[j].fixPrice){
               let temp = tempArr[i];
               tempArr[i] = tempArr[j];
               tempArr[j] = temp;
           }
       }
   }
   return tempArr;
}
